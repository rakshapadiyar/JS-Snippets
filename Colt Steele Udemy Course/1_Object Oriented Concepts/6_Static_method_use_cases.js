// related methods that we can group together, add, subtract,multiply,divide in Math class

class MyMath {
  static add(a, b) {
    return a + b;
  }
  static subtract(a, b) {
    return a - b;
  }
  static multiply(a, b) {
    return a * b;
  }
  static divide(a, b) {
    return a / b;
  }
}
MyMath.add(2, 3);
MyMath.multiply(2 * 3);
